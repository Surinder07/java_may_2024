public class ComparisonOp {
    public static void main(String[] args){
        // Comparison Op
        // == <= >= != < >

        // = -- assignment opeator
        // == -- equal operator

        int x = 10;
        int y = 20;
        int a = 15;
        int b = 25;
        //
       // boolean result = x == y;

        System.out.println(x == y); // false
        System.out.println(x < y); // true
        System.out.println(x > y); // false
        System.out.println(x >= y); // false
        System.out.println(x <= y); // true
        System.out.println(x != y); // true
        System.out.println(a > b); // false
        System.out.println(a < x); // false
        System.out.println( b == y); // false
        System.out.println(a >= y); // false
        System.out.println(b <= y); // false
        System.out.println(b != y); // true





    }
}
