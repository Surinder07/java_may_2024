package june11;

public class CharArrayDemo {
    public static void main(String[] args) {

        char[] arr = {'p','r','a','g','r','a'};

        for (char ch : arr) {
            System.out.print(ch);
        }

        System.out.println();

        String name = "pragra"; //  name -----------> pragra
        //
        name = "Vivek"; // name ------------------> Vivek
        System.out.println(name);


    }
}
