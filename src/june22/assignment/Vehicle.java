    public void setModel(String model) {
        this.model = model;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public void start(){

    }

    public void stop(){

    }
    public void displayInfo(){

        System.out.println(make);
        System.out.println(model);
        System.out.println(year);
    }


